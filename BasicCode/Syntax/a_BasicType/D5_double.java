package a_BasicType;

public class D5_double {
	public static void main(String[] args) {
		// 기본적으로 float의 리터럴은 숫자 뒤에 f를 표기합니다. 만약 f가 없다면, double type으로 인식합니다.

		float f = 5.5f; // 숫자 뒤에 f가 붙었기 때문에, float라고 인식합니다.
		System.out.println("Float: " + f);

		double d = 6.5;
		System.out.println("Double: " + d);
	}
}
