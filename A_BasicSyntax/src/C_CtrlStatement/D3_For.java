package C_CtrlStatement;

/**
 * 
 * for문법:
 * 		for(초기값;조건식;증감식){
 * 			바디;
 * 		}
 * 	
 * 	실행순서가 중요합니다. 반복문 중에서 가장 많이 쓰이는 구조입니다.
 * 		초기값 -> 바디 -> 증감식 -> 조건식 
 * 			-> (참이면 반복) 바디 -> 증감식 -> 조건식 
 * 			-> (참이면 반복) 바디 -> 증감식 -> 조건식
 *
 */

public class D3_For {
	public static void main(String[] args) {
		for (int i = 0; i < 10; i++) {
			System.out.println("for: " + i);
		}
	}
}

